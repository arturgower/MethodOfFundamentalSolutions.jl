# Laplace equation on the unit disk: watch the VariationalBayesianSolver learn the sources.
#
# The setup is the one of test/variational_test.jl ("ARD with source position updates
# learns the sources"): 10 true sources on a circle of radius 1.1, each with a Gaussian
# amplitude aᵢ ~ N(μᵢ, vᵢ). The boundary data carry the mean field and its variance
#     u(x) = Σᵢ μᵢ G(x - χᵢ),   Var u(x) = Σᵢ vᵢ G(x - χᵢ)²,
# as a vector of MvNormals, one per boundary point. The solver starts from 60 candidate
# sources on a ring of radius 1.35 and, iteration by iteration, moves them (M-step over χ)
# and prunes the ones whose learned prior variance 1/αᵢ collapses (ARD).
#
# Running this file saves, into figures/:
#   sources_iter_###.png  — one frame per snapshot: the field generated by the current
#                           sources, the sources sized by their learned prior std 1/√αᵢ,
#                           the initial candidates, and the true sources;
#   source_learning.gif   — the frames as an animation;
#   field_comparison.png  — the true mean field next to the final learned field.
#
# Needs Plots.jl (not a dependency of this package): julia> include(this file) from an
# environment where `using Plots` works, e.g. the default environment.

using MethodOfFundamentalSolutions
using Distributions, LinearAlgebra, StaticArrays, Statistics, Random
using Plots
gr()

outdir = joinpath(@__DIR__, "figures")
mkpath(outdir)

# ------------------------------------------------------------------ the true sources
medium = LaplaceMedium{2, Float64}()
FT = DirichletType()
Random.seed!(1234)

N_sources = 10
source_θs = LinRange(0, 2pi, N_sources + 1)[1:N_sources] + rand(N_sources) * 0.1
source_pos = [[1.1 * cos(θ), 1.1 * sin(θ)] for θ in source_θs]

# want all amps to same strength, as then they can be learned.
signs = [ θ > pi ? 1.0 : -1.0  for θ in source_θs]
source_amps = (-1.0) .^ signs
source_vars = (0.05 .* source_amps) .^ 2   # a 5% standard deviation per source

# --------------------------------------------------- boundary data with the field variance
N_points = 40
points_θs = LinRange(0, 2pi, N_points + 1)[1:N_points]
points = [[cos(θ), sin(θ)] for θ in points_θs]

# 1) evaluate the field of the combined sources: its mean and its variance
field_mean(x) = sum(source_amps[i] * greens(FT, medium, x - source_pos[i]) for i in eachindex(source_pos))
field_var(x) = sum(source_vars[i] * greens(FT, medium, x - source_pos[i])^2 for i in eachindex(source_pos))

bd = BoundaryData(FT;
    boundary_points = points,
    fields = [MvNormal([field_mean(x)], field_var(x) * I(1)) for x in points]
)

