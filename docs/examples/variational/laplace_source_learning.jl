# ------------------------------------------------------------------ candidate sources
N_dense = 60
dense_θs = LinRange(0, 2pi, N_dense + 1)[1:N_dense]
sources0 = [[1.35 * cos(θ), 1.35 * sin(θ)] for θ in dense_θs]

solver_upto(k) = VariationalBayesianSolver(
    prior_variance = 1.0,
    optimise_source_positions_flag = true,
    ard_threshold = 1e6,
    max_iters = k,
    elbo_tol = 1e-9
)

# ------------------------------------------------------------------------ plotting
# The interior field heatmap and the retained sources (sized by their learned prior std
# 1/√αᵢ, the ones shrinking being about to be pruned by ARD) come straight from the
# `VariationalSolution`/`FundamentalSolution` + `BoundaryData` plot recipes in plot/plot.jl;
# `bd` supplies both the field type and the disk that masks the heatmap. Here we only add
# the overlays specific to this example: the initial candidates and the true sources.
res = 141                          # grid resolution for the interior heatmaps
plotlims = (-1.55, 1.55)

# the true sources as a FundamentalSolution, so predict_field / the recipe draw their field
fsol_true = FundamentalSolution(medium; positions = source_pos, coefficients = source_amps)

# shared color limits from the true field over the interior of the disk (NaN outside)
fr_true = predict_field(fsol_true, bd; xres = res, yres = res)
clims = extrema(filter(isfinite, first.(field(fr_true))))

marker_true = (:xcross, 7, :black)
color_initial = "#6b7280"

# overlay the initial candidates and the true sources onto an existing field plot
function add_reference_sources!(plt)
    scatter!(plt, [p[1] for p in sources0], [p[2] for p in sources0];
        marker = (:circle, 2.5), markercolor = :white, markerstrokecolor = color_initial,
        label = "initial candidates")
    scatter!(plt, [p[1] for p in source_pos], [p[2] for p in source_pos];
        marker = marker_true, markerstrokewidth = 2, label = "true sources")
    return plt
end

function frame(vsol, k)
    plt = plot(vsol, bd;
        xres = res, yres = res,
        c = :inferno, clims = clims, colorbar_title = "field", legend = true,
        xlims = plotlims, ylims = plotlims,
        title = "iteration $k:  $(length(vsol.fsol.positions)) of $N_dense sources",
        titlefontsize = 11, size = (620, 560), background_color = :white
    )
    return add_reference_sources!(plt)
end

snapshots = [1, 2, 3, 4, 6, 8, 12, 16, 24, 40, 80, 150]
vsol_final = nothing
plts = map(enumerate(snapshots)) do (i, k)
    # the solver is deterministic, so re-solving with max_iters = k reproduces the
    # state of the full run after k iterations
    sim = Simulation(medium, bd; solver = solver_upto(k), source_positions = sources0)
    vsol = solve(sim)
    global vsol_final = vsol
    plt = frame(vsol, k)
    png(plt, joinpath(outdir, "sources_iter_" * lpad(k, 3, '0') * ".png"))
    println("iteration $k: $(length(vsol.fsol.positions)) sources retained")
    plt
end

anim = Animation()
foreach(plt -> Plots.frame(anim, plt), plts)
gif(anim, joinpath(outdir, "source_learning.gif"); fps = 2)

# ------------------------------------------------ the true field next to the learned one
# the FundamentalSolution + BoundaryData recipe draws the true field; sources = false since
# the true positions are drawn below as the xcross markers
plt_true = plot(fsol_true, bd;
    xres = res, yres = res, sources = false,
    c = :amp, clims = clims, colorbar = false,
    xlims = plotlims, ylims = plotlims,
    title = "true mean field", titlefontsize = 11
)
scatter!(plt_true, [p[1] for p in source_pos], [p[2] for p in source_pos];
    marker = marker_true, markerstrokewidth = 2, label = "true sources")

plt_learned = frame(vsol_final, snapshots[end])
plot!(plt_learned, title = "learned field and sources")

plt_cmp = plot(plt_true, plt_learned; layout = (1, 2), size = (1150, 520))
png(plt_cmp, joinpath(outdir, "field_comparison.png"))

println("saved figures to $outdir")
